#!/usr/bin/env node
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = resolve(root, 'docs/generated/implementation-reference.md');
const sourcePaths = [
  'src/types.ts',
  'src/hashing.ts',
  'src/context-manager.ts',
  'src/storage/storage-port.ts',
  'src/storage/sqlite-schema.ts',
  'src/storage/sqlite-storage.ts',
  'src/phase3-extension.ts',
  '.pi/live-drive/scm-live-drive.ts',
];

function stripCommentLine(line) {
  return line.replace(/^\s*\* ?/, '');
}

function trimBlankLines(lines) {
  let start = 0;
  while (start < lines.length && lines[start].trim() === '') start += 1;
  let end = lines.length;
  while (end > start && lines[end - 1].trim() === '') end -= 1;
  return lines.slice(start, end);
}

function nextSignature(source, offset) {
  const tail = source.slice(offset).split('\n');
  for (const raw of tail) {
    const line = raw.trim();
    if (!line) continue;
    if (line.startsWith('//')) continue;
    return line;
  }
  return '(signature unavailable)';
}

function extractDocSections(source, filePath) {
  const matches = [...source.matchAll(/\/\*\*([\s\S]*?)\*\//g)];
  const sections = [];

  for (const match of matches) {
    const content = match[1] ?? '';
    const lines = content.split('\n').map(stripCommentLine);
    const titleLine = lines.find((line) => line.startsWith('@impldoc'));
    if (!titleLine) continue;

    const title = titleLine.slice('@impldoc'.length).trim() || 'Untitled implementation section';
    const body = trimBlankLines(lines.filter((line) => !line.startsWith('@impldoc'))).join('\n').trim();
    const signature = nextSignature(source, (match.index ?? 0) + match[0].length);

    sections.push({
      title,
      body,
      signature,
      filePath,
      order: match.index ?? 0,
    });
  }

  return sections;
}

async function render() {
  const sections = [];

  for (const relPath of sourcePaths) {
    const absolutePath = resolve(root, relPath);
    if (!existsSync(absolutePath)) throw new Error(`Missing source file for docs generation: ${relPath}`);
    const source = await readFile(absolutePath, 'utf8');
    sections.push(...extractDocSections(source, relPath));
  }

  const lines = [
    '# Generated Implementation Reference',
    '',
    '> Generated from source docstrings by `npm run docs:generate`.',
    '> Do not edit this file by hand.',
    '',
    'Implementation authority lives in source docstrings. Behavioral intent authority remains `docs/intent-ssot-v1.md`.',
    '',
    '## Source files',
    '',
    ...sourcePaths.map((relPath) => `- \`${relPath}\``),
    '',
  ];

  for (const section of sections) {
    lines.push(`## ${section.title}`);
    lines.push('');
    lines.push(`- Source: \`${section.filePath}\``);
    lines.push(`- Decl: \`${section.signature.replace(/`/g, '\\`')}\``);
    lines.push('');
    if (section.body) lines.push(section.body, '');
  }

  return `${lines.join('\n').trim()}\n`;
}

const checkMode = process.argv.includes('--check');
const rendered = await render();

if (checkMode) {
  const current = existsSync(outputPath) ? await readFile(outputPath, 'utf8') : null;
  if (current !== rendered) {
    console.error(`Implementation docs are out of date: ${relative(root, outputPath)}`);
    console.error('Run: npm run docs:generate');
    process.exit(1);
  }
  console.log(`Implementation docs are up to date: ${relative(root, outputPath)}`);
  process.exit(0);
}

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, rendered, 'utf8');
console.log(`Wrote ${relative(root, outputPath)}`);
